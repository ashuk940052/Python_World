Markes = int(input("Enter the Studen Marks :- "))

if Markes >= 90:
    print("Student Scored 'A'")

elif 80<= Markes <90 :
    print("Student Scored 'B'")

elif 70<= Markes < 80 :
    print("Student Scored 'C'")  

elif Markes < 70 :
    print("Student Scored 'D'") 


